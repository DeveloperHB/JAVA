package chap03.sec10;

public class AssignmentOperatorExample {

	public static void main(String[] args) {
		int a = 0;
		
		a +=10; //a = a+10;
		a *=10; //a = a*10;
		a -=10; //a = a-10;
		a /=10; //a = a/10;
		a++;
		
		int result = 0;
		result += 10;
		System.out.println("result =" +result  );
		
		result -= 10;
		System.out.println("result =" +result  );
	

	}

}
